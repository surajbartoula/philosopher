#include "philo.h"

int	main(int argc, char *argv[])
{
	t_program	program;
	t_philo		philo;

	if (parse_input(argc, argv) > 0)
		return (0);
	init_program(&program);
	init_forks(forks, ft_atoi(argv[1]));
	init_philos(philos, &program, forks, argv);
	thread_create(&program, forks);
	destroy_all(NULL, &program, forks);
	return (0);
}
